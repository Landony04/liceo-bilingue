import mongoose from "mongoose";

const directorSchema = mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
});

const Teacher = mongoose.model("Director", directorSchema);

export default Teacher;
